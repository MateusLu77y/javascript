 /* Temos vários operadores aritméticos veja:
        Adição (+)
        Subtração (-)
        Multiplicação (*)
        Divisão (/)
        Módulo (%) (resto da divisão)
        Exponencial (**) (pontencialização)
        Incrementar (++) (adicionar uma unidade)
        Decrementar (--) (subtrair uma unidade)
*/
        //Exemplos de operaçãoes:
        
        let n1 = 4 //Variável n1 recebe o valor de 4.
        let n2 = 5 //Vartiável n2 recebe o valor de 5.

        let adição = n1 + n2 // 4 + 5 = 9
        let subtração = n1 - n2  // 4 - 5 = -1 
        let multiplicação = n1 * n2 // 4 x 5 = 20
        let divisão = n1 / n2 // 4 / 5 = 0.8
        let modulo = n1 % n2 // 4 / 5 = 4
        let exponencial = n1 ** n2 // 4 ^ 5 = 1024
        let incrementar = n1 ++ // 4 + 1 = 5
        let decrementar = n1 -- // 4 - 1 = 3