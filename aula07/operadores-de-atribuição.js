/* Operadores de atribuição, são usados para atribuir valores a variáveis */

// Exemplo de operadores de atribuição:

let n1 = 5 // operador de recebe (=)
n1 += 5 // n1 = n1 + 5 (forma normal)
n1 -= 5 // n1 = n1 - 5 (forma normal)
n1 *= 5 // n1 = n1 * 5 (forma normal)
n1 /= 5 // n1 = n1 / 5 (forma normal)
n1 %= 5 // n1 = n1 % 5 (forma normal)
n1 **= 5 // n1 = n1 ** 5 (forma normal)

