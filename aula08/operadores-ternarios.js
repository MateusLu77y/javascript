/* Operadores ternários funcionam da mesma forma se, e senão, se uma condição for verdadeira, será aplicada uma reposta (saída), se for falsa, será aplicada outra.
 */

//Operadores:
/*
Se (?) (se uma condição for verdadeira!)
Se não (:) (se uma confição não for verdadeira!

//exemplo:
*/

let idade = 19
let nota = 8.6
let nome = 'Pedro'

console.log(`A Pessoa tem ${idade} anos ela é adulta? ${19 >= 18 ? 'Sim' : 'Não'}`)
console.log(`A pessoa tirou ${nota} na prova, ela foi aprovada? ${8.6 >= 7 ? 'Sim' : 'Não'}`)
console.log(`O nome dessa pessoa é Maria? ${nome === 'Maria' ? 'Sim' : 'Não'}`)